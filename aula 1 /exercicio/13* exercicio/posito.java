public class posito {
    public static void main(String[] args) {
        int num  = 10;

        if (num > 0 ){
            System.out.println("the  number  is positive");
        }else if (num < 0 ){
            System.out.println("the number is negative");
        }else if (num == 0){
            System.out.println("the number is zero");
        }else{
            System.out.println("Error: enter a number");
        }
    }
}
